    private void addlblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addlblMouseClicked
        BatchBean batch = new BatchBean();
        if (batchtable.getSelectedRow() == -1) {
            if (!batchtxt.getText().isEmpty() && !batchtxt.getText().equalsIgnoreCase("Enter batch name here")) {
                batch.setBatchName(batchtxt.getText());
                batch.setCreatedBy(emp.getEmpId());
                batch.setCreatedDate(CurrentDate.getCurrentDate() + "");
                if (batchDao.addBatch(batch) == 1) {
                    populateBatchTable();
                    batchtxt.setText("Enter batch name here");
                } else {
                    new MessageForm("Error", "Batch not added", "error.png").setVisible(true);
                }
            }
        } else {
            new MessageForm("Error", "Record is duplicate", "error.png").setVisible(true);
        }
    }//GEN-LAST:event_addlblMouseClicked

    private void deletelblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deletelblMouseClicked
        if(batchtable.getSelectedRow()!=-1){
        BatchBean batch = new BatchBean();
        batch.setBatchId(Integer.parseInt(batchtable.getValueAt(batchtable.getSelectedRow(), 0).toString()));
        batch.setModifiedBy(emp.getEmpId());
        batch.setModifiedDate(CurrentDate.getCurrentDate() + "");
        if (batchDao.deleteBatch(batch) == 1) {
            populateBatchTable();
            clearFields();
        } else {
            new MessageForm("Error", "Batch not deleted", "error.png").setVisible(true);
        }
        }else{
            new MessageForm("Error","No record selected","error.png").setVisible(true);
        }
    }//GEN-LAST:event_deletelblMouseClicked

