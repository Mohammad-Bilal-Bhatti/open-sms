    private void populateBatchTable() {
        List<BatchBean> batches = batchDao.getAllBatches();
        DefaultTableModel dtm = (DefaultTableModel) batchtable.getModel();
        dtm.setRowCount(0);
        for (BatchBean batch : batches) {
            Object[] values = {batch.getBatchId(), batch.getBatchName()};
            dtm.addRow(values);
        }
        //UtilityMethods.fillDataIntoTables(rs, batchtable, jScrollPane1, this);
    }

    private void clearFields() {
        batchtxt.setText("Enter batch name here");
        batchtable.clearSelection();
    }

